"""
Приклад 5.7. У заданий текст входять тільки цифри та літери.
Визначити, чи правда, що сума числових значень цифр,
які входять у текст, дорівнює довжині тексту.
"""

S = input("Введіть вираз ")
m = 0
for c in S:
    if c >= '0' and c <= '9':
        m = m + int(c) # тут використовується операція int(c)
                       # перетворення символа c у число

if len(S) == m:
    print("Так")
else:
    print("Ні")
