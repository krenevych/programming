"""
Приклад 5.14. Перевірити чи слова, що записані у рядку знаходяться у словниковому порядку.
"""

S = input("Введіть рядок ")
l = S.split()
ok = True
for i in range(1, len(l)):
    if l[i - 1] > l[i]:
        ok = False
        break

if ok:
    print("Слова в алфавітному порядку")
else:
    print("Слова НЕ в алфавітному порядку")


