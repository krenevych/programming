"""
Приклад 8.3. Опишемо функцію, що знаходить суму елементів заданої послідовності.
"""


def suma(*elements):
    res = 0
    # Оскільки elements – кортеж скористаємося циклом for
    for el in elements:
        res += el
    return res

# Головна програма полягає у виклиці функції suma.
print(suma(1, 2, 3, 4, 5))






